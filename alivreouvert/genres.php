                <h1>Les genres litt&eacute;raires</h1>
                <p>Un genre littéraire est un concept de type catégoriel qui permet de classer des productions littéraires en prenant en compte des aspects de genre pictural, genre narratif ou genre dramatique, de contenu (entre autres : roman d'aventure, journal intime, théâtre de boulevard, etc.), ou encore de registre (fantastique, tragique, comique notamment). Divers critères pouvant se combiner et se chevaucher pour déterminer des catégories secondaires, la liste des genres n’étant en effet pas close. Le débat sur la constitution des genres littéraires existe depuis Platon et surtout depuis l'ouvrage majeur en la matière d'Aristote : la Poétique .</p>

                <p>Le fait d’inscrire une œuvre dans un genre est une façon de répondre à l'horizon d'attente d'un public donné, pour reprendre la formule de Hans Robert Jauss. Selon la façon dont une œuvre est présentée (roman, autobiographie, comédie, drame...), le lecteur s’en fait une représentation plus ou moins stéréotypée, qui peut cependant être remise en question lors de la lecture. Le genre est donc, avant tout, une convention qui donne un cadre au public et fonctionne comme un modèle d'écriture pour les auteurs. C'est ce que souligne Tzvetan Todorov : « Chaque époque a son propre système de genres, qui est en rapport avec l'idéologie dominante. Une société choisit et codifie les actes qui correspondent au plus près à son idéologie ; c'est pourquoi l'existence de certains genres dans une société, leur absence dans une autre, sont révélatrices de cette idéologie. »</p>

                <p>Un genre littéraire est aussi un premier échange entre l'auteur et le lecteur qui se fait au moyen du paratexte. L'étiquetage du genre est parfois délicat à déterminer comme pour l'autofiction qui joue sur réalité et imaginaire entre roman et autobiographie, le roman à thèse comme Le Dernier Jour d'un condamné qui appartient à la fois au genre du roman et au genre argumentatif, ou encore pour l'épopée, genre à la fois narratif et versifié.</p>

                <p>Les genres sont avant tout une classification commode à manier en pédagogie, permettant d'appréhender les caractéristiques des productions littéraires. Nombre d'auteurs et d'universitaires ont proposé des classifications.</p>


                <h2>Les genres poétiques</h2>
                <p>Les genres poétiques présentent une grande variété des formes qui ont évolué avec les siècles. Ils sont pour l'essentiel associés à des conventions techniques et typographiques comme la mise en page des vers ou les différents mètres. Caractérisés par le travail de la forme, ils se singularisent par la musicalité, l'expression des émotions et la force de suggestion (images).</p>

                <h2>Les genres narratifs</h2>
                <p>Les genres narratifs sont caractérisés par le récit de l'enchaînement plus ou moins complexe des événements, des péripéties avec la possibilité d'en établir le schéma narratif et de définir le principe général de l'action par le schéma actanciel qui expose les différents rôles présents dans le récit. On peut également définir le statut du narrateur (ou des narrateurs), distinct(s) de l'auteur sauf mention particulière, ainsi que les points de vue narratifs choisis et la structure chronologique de l'œuvre. Polymorphes, les genres narratifs exploitent aussi bien les différents discours (direct, indirect, indirect libre) et la description (cadre spatio-temporel, portraits) que le récit proprement dit (péripéties), le commentaire ou l'expression poétique. Ils se déterminent aussi par leur longueur, leur rapport au réel et au fictionnel ainsi que par leur objet comme l'écriture de soi dans l'autobiographie.</p>
                <h2>Les genres théâtraux</h2>
                <p>Les genres théâtraux sont marqués par l'oralité et l'éphémère, par la double énonciation et la fonction du comédien et aussi par les données pratiques du spectacle. Ils sont souvent catégorisés en sous-genres plaisants (comédie) ou sérieux (tragédie).</p>
                <h2>Les genres épistolaires</h2>
                <p>Les genres épistolaires consistent en correspondance authentique où l'on rencontre aussi bien la confidence que la prise de position, et les lettres fictives, avec le cas particulier du roman par lettres (ex. Les Liaisons dangereuses) délicat à catégoriser.</p>
                <h2>Les genres argumentatifs</h2>
                <p>Ils cherchent à convaincre et à séduire en prenant parti avec des stratégies argumentatives variées incluant le traitement des thèses en présence (modalisation), le rapport entre le locuteur et le destinataire comme dans le maniement des arguments et des exemples.</p>
                <h2>Les genres descriptifs</h2>
                <p>Le genre est unique : la galerie de portraits. Les Caractères, de La Bruyère, accompagnés d'une visée argumentative, sont classés comme telle.</p>
